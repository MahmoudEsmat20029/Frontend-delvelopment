function getOpposite(num) {
  /* =========== First way =========== */
  return -num;
}

console.log(getOpposite(1)); // -1
console.log(getOpposite(2)); // -2
console.log(getOpposite(3)); // -3
console.log(getOpposite(4)); // -4
console.log(getOpposite(5)); // -5
console.log(getOpposite(-1)); // 1
console.log(getOpposite(-2)); // 2
console.log(getOpposite(-3)); // 3
console.log(getOpposite(-4)); // 4
console.log(getOpposite(-5)); // 5
